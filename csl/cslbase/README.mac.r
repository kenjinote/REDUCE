The file "mac.r" is used with the Macintosh System X version of this
code to add a resource fork to binaries and make them launchable by
clicking. It is a verbatim copy of part of FLTK (www.fltk.org), and as such
is licensed under the LGPL but with a range of generous extensions the
permit static linking, do not require that the full FLTK license be distributed
and generally make things utterly non-onerous. By keeping this comment
in a separate file I do not need to modify the "mac.r" file at all.



